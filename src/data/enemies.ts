// 根据ID获取敌人
export function getEnemyById(id: string): ExpandedEnemy | undefined {
  return ENEMIES.find(e => e.id === id);
}

// 根据站台和类型获取敌人列表
export function getEnemiesByLocation(locationId: string, type?: 'normal' | 'elite' | 'boss'): ExpandedEnemy[] {
  let enemies = ENEMIES.filter(e => e.locationId === locationId);
  if (type) {
    enemies = enemies.filter(e => e.enemyType === type);
  }
  return enemies;
}

// 随机获取指定站台的敌人
export function getRandomEnemyByLocation(locationId: string, type?: 'normal' | 'elite' | 'boss'): ExpandedEnemy | null {
  const enemies = getEnemiesByLocation(locationId, type);
  if (enemies.length === 0) return null;
  return enemies[Math.floor(Math.random() * enemies.length)];
}

// 创建敌人实例（深拷贝）
export function createEnemyInstance(enemyId: string): ExpandedEnemy | null {
  const template = getEnemyById(enemyId);
  if (!template) return null;
  
  return {
    ...template,
    hp: template.maxHp,
  };
}